package zadanie_1;

public class Vehicle {
    String color;
    int wheels;
    int tankCapacity;
    int enginePower;

    public Vehicle(String color, int wheels, int tankCapacity, int enginePower) {
        this.color = color;
        this.wheels = wheels;
        this.tankCapacity = tankCapacity;
        this.enginePower = enginePower;
    }
}
